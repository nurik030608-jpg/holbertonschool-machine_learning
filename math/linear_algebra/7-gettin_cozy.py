#!/usr/bin/env python3
"""
Модуль для конкатенации 2D матриц по заданной оси.
"""


def cat_matrices2D(mat1, mat2, axis=0):
    """
    Конкатенирует две матрицы по оси 0 или 1.
    Возвращает новую матрицу или None, если размеры не позволяют склейку.
    """
    # Склеивание по вертикали (добавляем новые строки)
    if axis == 0:
        # Проверяем, совпадает ли ширина (количество столбцов)
        if len(mat1[0]) != len(mat2[0]):
            return None
        # Создаем копии строк, чтобы не изменять оригиналы
        new_matrix = [row[:] for row in mat1] + [row[:] for row in mat2]
        return new_matrix

    # Склеивание по горизонтали (расширяем существующие строки)
    elif axis == 1:
        # Проверяем, совпадает ли высота (количество строк)
        if len(mat1) != len(mat2):
            return None
        # Создаем новые строки, склеивая соответствующие списки
        return [mat1[i] + mat2[i] for i in range(len(mat1))]

    return None
